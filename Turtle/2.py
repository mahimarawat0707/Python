import turtle

square = turtle.Turtle()
square.speed(200)
square.color("red")

for i in range(90):
  #triange
  square.forward(100)
  square.left(120)
  square.forward(100)
  square.left(120)
  square.forward(100)
  square.left(125)
  #circle
  square.circle(100)
  square.left(10)
  #pentagon
  square.forward(100)
  square.left(60)
  square.forward(100)
  square.left(90)
  square.forward(100)
  square.left(60)
  square.forward(100)
  square.left(80)
  square.forward(90)
  square.right(100)
  
turtle.done()

